  // ---------- locale detection ----------
  function detectLocale() {
    // 1. Check user-set preference in storage
    // 2. Check navigator.language
    // 3. Default to pt-BR
    const nav = (navigator.language || "pt-BR").toLowerCase();
    if (nav.startsWith("pt")) return "pt-BR";
    if (nav.startsWith("en")) return "en-US";
    if (nav.startsWith("zh")) return "zh-CN";
    if (nav.startsWith("es")) return "es";
    return "pt-BR";
  }

  let currentLocale = detectLocale();

  // ---------- t(key, params) ----------
  function t(key, params) {
    const locale = MESSAGES[currentLocale] ? currentLocale : "pt-BR";
    let str = MESSAGES[locale][key] || MESSAGES["pt-BR"][key] || key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
      }
    }
    return str;
  }

  // Pluralization helper: returns "" for singular, "s" for plural
  function plural(n) { return n === 1 ? "" : "s"; }

  // ---------- public API ----------
  window.__zaiI18n = {
    t,
    plural,
    getLocale: () => currentLocale,
    setLocale: (locale) => {
      if (MESSAGES[locale]) {
        currentLocale = locale;
        window.__zaiBus?.emit?.("i18n:locale-changed", { locale });
      }
    },
    getAvailableLocales: () => Object.keys(MESSAGES),
    // Re-render all UI (called after locale change)
    // Each module listens to "i18n:locale-changed" and re-renders itself
  };

  console.debug("[zai-enhancer] i18n ready, locale:", currentLocale);
